
## Classes
class ScientificCalculator:
    def add(self, num1, num2):
        return num1 + num2
    
    def sub(self, a, b):
        return a - b


class Calculator:
    def add_numbers(self, number1, number2):
        return number1 + number2
    
    def subtract_numbers(self, number1, number2):
        return number1 - number2
